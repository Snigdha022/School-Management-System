    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = txtuname.getText();
        String pass= new String(txtpass.getPassword());
        String usertype = txtutype.getSelectedItem().toString();
        
        try {
            pst = con.prepareStatement("select * from user where uname=? and password=? and utype=?");
            pst.setString(1, username);
            pst.setString(2, pass);
            pst.setString(3, usertype);
            rs= pst.executeQuery();
            if(rs.next()){
                int id= rs.getInt("id");
                this.setVisible(false);
                if(usertype.equals("Admin"))
                {
                    new Main(id,username,usertype).setVisible(true);
                    
                }
                else
                {
                    new TeacherMain(id,username,usertype).setVisible(true);
                }
                
                    
                    
                
            }
            else{
                JOptionPane.showMessageDialog(this, "Incorrect username or password");
                txtuname.setText(" ");
                txtpass.setText("");
                txtutype.setSelectedIndex(-1);
                txtuname.requestFocus();
            }
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

