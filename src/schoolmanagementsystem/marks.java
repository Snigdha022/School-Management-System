    private void button_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_searchActionPerformed
       
        String sid=txtno.getText();
        try {
            pst = con.prepareStatement("select * from student where stid=?");
            pst.setString(1,sid);
            rs = pst.executeQuery();
            if(rs.next()==false)
            {
                JOptionPane.showMessageDialog(this,"Student id is not found");
                txtstname.setText("");
            }
            else
            {
                String name = rs.getString("stname");
                txtstname.setText(name.trim());
                String classes = rs.getString("class");
                txtclass.addItem(classes.trim());
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(marks.class.getName()).log(Level.SEVERE, null, ex);
        }
        
       
    }//GEN-LAST:event_button_searchActionPerformed

    private void button_savemarksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_savemarksActionPerformed
        String stid=txtno.getText();
        String classes=txtclass.getSelectedItem().toString();
        String subject=txtsubject.getSelectedItem().toString();
        String marks=txtmarks.getText();
        String term=txtterm.getSelectedItem().toString();
        
        
        
        try {
            pst= con.prepareStatement("insert into marks(studentid,class,subject,marks,term)values(?,?,?,?,?)");
            pst.setString(1, stid);
            pst.setString(2,classes);
            pst.setString(3,subject);
            pst.setString(4,marks);
            pst.setString(5,term);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Marks is added successfully");
            
            
        } catch (SQLException ex) {
            Logger.getLogger(exam.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_button_savemarksActionPerformed

