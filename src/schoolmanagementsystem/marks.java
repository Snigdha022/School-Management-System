    private void button_meditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_meditActionPerformed
   
        
        
        try {
            
            d = (DefaultTableModel)jTable1.getModel();
            int selectIndex = jTable1.getSelectedRow();
            String id = d.getValueAt(selectIndex, 0).toString();
            String stid=txtno.getText();
            String classes=txtclass.getSelectedItem().toString();
            String subject=txtsubject.getSelectedItem().toString();
            String marks=txtmarks.getText();
            String term=txtterm.getSelectedItem().toString();
        
            pst= con.prepareStatement("update marks set studentid=?, class=?, subject=?, marks=?, term=? where id=?");
            pst.setString(1, stid);
            pst.setString(2,classes);
            pst.setString(3,subject);
            pst.setString(4,marks);
            pst.setString(5,term);
            pst.setString(6,id);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Marks is edited successfully");
            button_savemarks.setEnabled(true);
            txtno.setText("");
            txtstname.setText("");
            txtclass.setSelectedIndex(-1);
            txtsubject.setSelectedIndex(-1);
            txtmarks.setText("");
            txtterm.setSelectedIndex(-1);
            txtno.requestFocus();
            
            marks_Load();
            
            
        } catch (SQLException ex) {
            Logger.getLogger(exam.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_button_meditActionPerformed

